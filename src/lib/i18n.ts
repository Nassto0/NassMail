const DICT: Record<Lang, Dict> = { en, ar };

export function translate(lang: Lang, key: string, vars?: Record<string, string>) {
  let v = DICT[lang]?.[key] ?? DICT.en[key] ?? key;
  if (vars) Object.entries(vars).forEach(([k, val]) => { v = v.replace(`{${k}}`, val); });
  return v;
}
